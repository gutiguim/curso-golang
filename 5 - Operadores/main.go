package main

import (
	"fmt"
)

func main() {
	fmt.Println("OPERADORES")
	// aritméticos +, -, *, /, %
	fmt.Println("Aritméticos:")
	soma := 10 + 1
	fmt.Println("soma")
	fmt.Println(soma)

	subtracao := 10 - 1
	fmt.Println("subtracao")
	fmt.Println(subtracao)

	divisao := 10 / 2
	fmt.Println("divisao")
	fmt.Println(divisao)

	multiplicacao := 10 * 3
	fmt.Println("multiplicacao")
	fmt.Println(multiplicacao)

	modulo := 10 % 3
	fmt.Println("modulo")
	fmt.Println(modulo)
}
